package junsProject0530;

public class StockInfo {
	
	private int stock_number;
	private String stock_name;
	private int stock_volume;
	private String stock_unit;
	private int stock_consume;
	public int getStock_number() {
		return stock_number;
	}
	public void setStock_number(int stock_number) {
		this.stock_number = stock_number;
	}
	public String getStock_name() {
		return stock_name;
	}
	public void setStock_name(String stock_name) {
		this.stock_name = stock_name;
	}
	public int getStock_volume() {
		return stock_volume;
	}
	public void setStock_volume(int stock_volume) {
		this.stock_volume = stock_volume;
	}
	public String getStock_unit() {
		return stock_unit;
	}
	public void setStock_unit(String stock_unit) {
		this.stock_unit = stock_unit;
	}
	public int getStock_consume() {
		return stock_consume;
	}
	public void setStock_consume(int stock_consume) {
		this.stock_consume = stock_consume;
	}

}
